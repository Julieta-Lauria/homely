<br>

Poly & Bark was born out of necessity on a newly-rented apartment floor, over a box of pizza. When decorating our new space we found it difficult to find a manufacturer who’s affordability matched the level of quality and design we were looking for. We found ourselves wedged into a choice of purchasing furniture at a high-cost and mediocre quality or buying low-cost with weak durability.

<br>
<br>
We’re selective about what we present to our customers, and have carefully cultivated a collection that combines contemporary flair with iconic inspirations to fit the modern lifestyle. Poly & Bark is driven to be relaxed and appealing, inviting and sophisticated.</p>

